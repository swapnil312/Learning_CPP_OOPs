# include <iostream>
using namespace std;
// << -> Insertion operator(insert to terminal/console)
// >> -> Extraction operator(extract from terminal/console)
int main()
{
    int num1, num2;
    cout<<"Enter value of num1: ";
    cin>>num1;
    cout<<"Enter value of num1: ";
    cin>>num2;
    cout<<"Sum of "<<num1<<" and "<<num2<<" is "<<(num1+num2)<<".";
    return 0;

}